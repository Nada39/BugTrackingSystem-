    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
           try{
         Class.forName("com.mysql.cj.jdbc.Driver"); 
         con = DriverManager.getConnection(db , username, password);
         String sql = "select * from Bugs where Fixed='fixed'";
         stat = con.prepareStatement(sql);
         
         ret = stat.executeQuery();
         ResultSetMetaData stData = ret.getMetaData();
         q = stData.getColumnCount();
         
         DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
         RecordTable.setRowCount(0);
         while(ret.next())
            {
                Vector col = new Vector();
                for(i = 1; i <= q; i++)
                {
                    col.add(ret.getString("bugsID"));
                    col.add(ret.getString("bugtitle"));
                    col.add(ret.getString("status"));
                    col.add(ret.getString("fixed"));
                }
                RecordTable.addRow((col));
            }
        
         con.close();
        }catch(Exception e){
            System.out.println(e.getMessage());
        }   
                
    }//GEN-LAST:event_jButton1ActionPerformed

